function Image:run()
	for rowidx,row in ipairs(self.image) do
		for colidx = 1,#row do
			local pixc = string.byte(row:sub(colidx,colidx))
			if pixc >= 10 then
				pixc = pixc - 16
			end
			--pix(colidx-1, rowidx-1, pixc)
			poke4(((rowidx-1)*240)+(colidx-1),pixc)
		end
	end

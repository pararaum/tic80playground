Class={}
function Class:extend(classinit)
	classinit.extends = self
	return setmetatable(classinit or {}, { __index=self})
end
function Class:new(init)
	local obj =  {}
	for k,v in pairs(init or {}) do
		obj[k]=v
	end
	setmetatable(obj, { __index=self })
	local curr, lastinit = self
	while curr do
		if curr.init and curr.init ~= lastinit then
			lastinit = curr.init
			lastinit(obj)
		end
		curr = curr.extends
	end
	return obj
end


Scroller = Class:extend({
		background = 11,
		foreground = 7,
		x = 240,
		y = 60,
		delta = 1
})
function Scroller:finish()
	if self.next then
		return self.next
	else
		return true
	end
end
function Scroller:run()
	cls(self.background)
	local width = print(self.text, self.x, self.y, self.foreground)
	self.x = self.x - self.delta
	if self.x < -width then
		return self:finish()
	end
end


DelayFrames = Class:extend({ delay=60 }) -- Default delay is 1s.
function DelayFrames:run()
	self.delay = self.delay - 1
	if self.delay < 0 then
		return true
	end
end


Rasterbar = Class:extend({ y=80, A=20, phi=0, omega=.06, frame=4*60 })
function Rasterbar:run()
	cls()
	for idx, col in ipairs(self.colours) do
		local y = self.y + idx + self.A * math.sin(self.phi)
		line(0, y, 239, y, col)
	end
	self.phi = self.phi + self.omega
	self.frame = self.frame - 1
	return self.frame < 0
end


RasterbarWText = Rasterbar:extend({textx=0, texty=10, textcol=13})
function RasterbarWText:run()
	ret = self.extends.run(self)
	print(self.text, self.textx, self.texty, self.textcol)
	return ret
end



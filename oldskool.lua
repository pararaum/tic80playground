function TIC()
	local new={}
	for _,eff in ipairs(EFFECTS) do
		local ret=eff:run()
		if ret then
			if ret ~= true then
				table.insert(new, ret)
			else
				table.insert(new, next_effect())
			end
		else
			table.insert(new, eff)
		end
	end
	EFFECTS=new
end



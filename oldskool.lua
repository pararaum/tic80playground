function Image:new(init)
	local ret = self.extends.new(self, init)
	local oldcols = {}
	for i = 1, 16 do
		local colstr = self.colors[i]
		local r = tonumber("0x"..colstr:sub(1,2))
		local g = tonumber("0x"..colstr:sub(3,4))
		local b = tonumber("0x"..colstr:sub(5,6))
		table.insert(oldcols, peek(0x3fc0-1 + i))
		poke(0x3fc0-3 + 3*i, r)
		poke(0x3fc0-2 + 3*i, g)
		poke(0x3fc0-1 + 3*i, b)
	end
	ret.old_colors = oldcols
	return ret
